list = [2,7,5,11,1,9,4]

list.sort() #sort the list in ascending order

list.reverse() #reverse the list

print(list[:5]) #print the list from 0 to 4 

print(list[2:6]) #print the list from 2 to 5

print(list[::-1]) #print the list in reverse order

list.append(99) #add element 99 to the list

list.insert(2,100) # insert 100 at index 2

list.remove(1) #remove 1 from list

list.pop() # remove last element from list (pop operation)

